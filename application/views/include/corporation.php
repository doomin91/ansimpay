                                    <p>2. 타 회원 정보를 무단으로 부정 사용하거나 타인으로 가장, 또는 관계를 허위로 명시하는 행위</p>
                                    <p>3. 회사가 게시한 정보의 무단 변경 행위</p>
                                    <p>4. 회사가 정하지 않은 정보의 사전동의 없는 복제, 유통, 송신, 게시, 수집, 저장, 공개 행위</p>
                                    <p>5. 회사의 공식적 동의 없는 상업적인 서비스 이용 및 거짓 정보 유통 행위</p>
                                    <p>6. 회사 또는 기타 제3자의 저작권 등 지적재산권 등 기타 권리를 침해하는 행위</p>
                                    <p>7. 회사 또는 기타 제3자의 명예를 손상시키거나 고의로 서비스 운영을 방해하는 행위</p>
                                    <p>8. 서비스의 안정적 운영을 방해할 수 있는 정보 및 수신자의 명시적인 수신거부 의사를 거부한 광고성 정보 전송 행위</p>
                                    <p>9. 접근 권한 없이 회사의 전자 설비에 접근하거나 데이터를 파괴하거나 운영을 방해할 목적으로 컴퓨터 바이러스, 논리폭탄 또는 메일폭탄 등의 프로그램의 투입, 일시에 발생하는 대량의 신호, 고출력 전자기파 또는 데이터를 보내거나, 부정한 명령을 처리하도록 하는 등의 방법으로 회사의 전자 설비에 오류 또는 장애를 발생하게 하는 등의 전자적 침해행위</p>
                                    <p>10. 해킹, 광고를 통한 수익, 음란 사이트를 통한 영업 행위</p>
                                    <p>11. 기타 회사 또는 타인의 권리를 침해하거나 업무를 방해하는 행위</p>
                                    <p>12. 기타 불법적이거나 부당한 행위</p>
                                    <p>①이용자는 관계 법령, 본 약관의 규정, 이용안내 및 서비스 상에 공지한 주의사항, 회사가 통지하는 사항 등을 준수하여야 하며, 기타 회사의 업무에 방해되는 행위를 해서는 안됩니다.</p>
                                    <p>②이용자는 “안심페이”에 사용하는 신용카드 관련 정보와 결제 시 사용하는 결제비밀번호 등에 대해 관리책임이 있으며, 이를 제3자에게 제공, 누설하거나 이와 유사한 행위를 하여서는 아니 됩니다.</p>
                                    <p>③이용자는 자신의 명의가 도용되거나 “안심페이”가 제3자에게 부정하게 사용된 것을 인지한 경우 즉시 그 사실을 회사에게 통보하여야 합니다.</p>
                                    <p>④이용자가 본 조를 위반하여 발생하는 모든 책임은 이용자에게 있으며, 회사는 책임이 면제됩니다.</p>
                                    <p>제8조 (“안심페이 서비스”의 해지 및 이용제한)</p>
                                    <p>① 이용자가 “안심페이 서비스” 이용계약의 전체 또는 일부를 해지하고자 할 경우에는 회사가 정한 방법으로 해지신청을 하여야 하며, 이 경우 회사는 일정한 절차를 거쳐 이용계약을 해지합니다.</p>
                                    <p>② 회사는 이용자에게 다음 각 호 중 하나의 사유가 있는 경우 “안심페이 서비스” 이용계약을 해지하거나 이용을 제한할 수 있습니다.</p>
                                    <p>1. 이용자가 본 약관을 위반한 사실이 밝혀진 경우</p>
                                    <p>3. 이용자에게 본 약관 제4조 제4항에서 정한 사유가 있는 경우</p>
                                    <p>4. 이용자가 컴퓨터바이러스 프로그램 등을 유포하거나, 해킹에 이용하는 등 “안심페이 서비스” 이용목적에 반하거나 불합리한 용도로 이용한 경우</p>
                                    <p>5. 기타 관계법령이나 회사의 다른 이용약관에 위배되는 행위를 하거나, 회사의 원활한 서비스 제공을 방해한 경우</p>
                                    <p>①이용자는 제2항 각호 중 하나에 해당하는 행위를 한 경우 이로 인하여 회사 또는 제3자에게 발생한 손해를 배상하여야 합니다.</p>
                                    <p>②“안심페이 서비스” 이용계약을 해지하고자 하는 이용자는 “안심페이 서비스” 전부를 해지할 수 있습니다.</p>
                                    <p>제9조 (면책)</p>
                                    <p>① 회사는 전자지급결제대행서비스를 제공하는 자로서, “안심페이”를 이용한 전자지급결제의 내용에 관한 분쟁에 대해서는 당해 이용자와 신용카드 회사 또는 금융기관이 책임을 져야 합니다.</p>
                                    <p>③ 회사는 천재지변 기타 불가항력의 사유, 제5조 제5항의 사유, 또는 이와 유사한 사유 등으로 인해 서비스가 제한, 중단되더라도 이로 인해 발생한 손해에 대해서는 책임이 없습니다.</p>
                                    <p>④ 회사는 이용자의 귀책사유로 인한 서비스 이용의 장애에 대하여 책임을 지지 않습니다.</p>
