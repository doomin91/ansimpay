                                    <p>5. “안심페이사이트”라 함은 이용자가 안심페이 서비스를 이용하거나 서비스 이용에 필요한 정보를 얻을 수 있도록 회사가 개발하여 운영하는 도메인주소로 하는 웹사이트를 말합니다.</p>
                                    <p>6. “서비스화면”이라 함은 안심페이사이트를 구성하는 화면의 일부 또는 전부를 말합니다.</p>
                                    <p>제3조 (약관의 적용범위, 명시 및 변경, 관련 법령과의 관계)</p>
                                    <p>① 회사는 이용자가 “안심페이 서비스”를 이용하기 전에 본 약관을 “안심페이 사이트”에 게시하고 이용자가 본 약관의 중요한 내용을 확인할 수 있도록 합니다.</p>
                                    <p>② 회사는 이용자의 요청이 있는 경우 전자문서 전송(전자우편을 이용한 전송을 포함합니다), 모사전송, 우편 또는 직접교부의 방식으로 본 약관의 사본을 이용자에게 교부합니다. 이용자가 원하는 경우 약관을 인쇄 또는 다운로드하는 방법으로 교부 받을 수 있습니다.</p>
                                    <p>③ 회사가 본 약관을 변경하는 때에는 그 시행일 7일 전(단, 이용자에게 불리한 변경인 경우 30일 전)에 변경되는 약관을 “안심페이 사이트”에 게시합니다. 다만, 법령의 개정으로 인하여 긴급하게 약관을 변경한 때에는 변경된 약관을 즉시 적용합니다. 이용자는 개정약관의 적용예정일까지 명시적으로 거부의 의사표시를 하지 아니한 경우 변경된 약관에 동의한 것으로 간주합니다. 이용자가 개정약관의 적용에 동의하지 않는 경우 회원은 이용계약을 해지할 수 있습니다.</p>
                                    <p>④ 본 약관에서 정하지 아니한 사항(용어의 정의 포함)에 대하여는 회사와 이용자 간 체결된 전자금융거래 이용약관을 보충적으로 적용하고, 전자금융거래 이용약관에서 정하지 않은 사항에 대해서는 회사가 운영하는 온라인가맹점에서 정한 약관에 따르고, 해당 약관에도 명시되지 않은 사항에 대해서는 전자금융거래법, 전자상거래 등에서의 소비자 보호에 관한 법률, 여신전문금융업법 등 소비자보호 관련 법령 및 개별약관에서 정한 바에 따릅니다. 그 외의 사항은 일반 상거래관행에 의합니다.</p>
                                    <p>제2장 전자지급결제대행 서비스</p>
                                    <p>제4조 (“안심페이” 이용계약의 성립)</p>
                                    <p>① 가맹점에서 “안심페이”를 이용하고자 하는 자는 본 약관에 대한 동의 및 본인인증을 하고 사용인증정보의 설정을 하여야 합니다.</p>
                                    <p>②“안심페이”를 이용하고자 하는 자가 제1항에 따라 본 약관에 대한 동의, 본인인증 및 안심페이 아이디 및 비밀번호를 설정함으로써 “안심페이”의 이용신청을 하였을 때 회사가 “안심페이”의 이용신청을 승낙함으로써 이용계약이 체결됩니다.</p>
                                    <p>③ “안심페이 서비스를 이용하고자 하는 자”는 제1항의 회원인증 및 본인인증 시 원칙적으로 본인이 직접 이행하여야 하며, 이를 타인으로 하여금 하게 하거나 인증번호 등의 정보를 노출하여서는 안됩니다.</p>
                                    <p>④ 회사는 다음의 각 호 중 하나에 해당하는 경우 이용신청을 승낙하지 않거나 승낙을 유보할 수 있습니다.</p>
                                    <p>1. “안심페이 서비스를 이용하고자 하는 자”가 허위의 정보를 입력 또는 제공하거나, 회사가 요구하는 내용을 입력, 제출 또는 확인하지 않은 경우</p>
                                    <p>2. 타인의 휴대폰번호를 도용하는 등 비정상적인 방법으로 등록하는 경우</p>
                                    <p>3. 타인의 공인인증정보 등 관련 정보를 도용하는 등 비정상적인 방법으로 등록하는 경우</p>
                                    <p>4. “안심페이 서비스를 이용하고자 하는 자”가 법령, 본 약관 및 기타 회사가 규정한 제반 사항을 위반하며 안심페이 이용을 신청하는 경우</p>
                                    <p>5. 이미 가입된 이용자와 이름 및 주민등록번호(또는 사업자등록번호, 법인등록번호), 이동통신 전화 번호 등 회원의 개인정보가 동일한 경우</p>
                                    <p>6. 기타 이용자로서 부적절한 행위를 할 우려가 있다고 인정되거나 “안심페이”를 정상적으로 이용할 수 없다고 합리적으로 판단되는 경우</p>
                                    <p>7. 서비스 관련 설비의 여유가 없거나, 기술상 또는 업무상 문제가 있는 경우</p>
                                    <p>⑤ 회사는 제4항에 따라 승낙을 하지 않거나 유보한 경우, 원칙적으로 이를 “서비스화면”에 게시하거나 “안심페이 서비스를 이용하고자 하는 자”에게 통지합니다.</p>
                                    <p>⑥ “안심페이”의 자세한 신청절차 및 구체적 내용은 ”서비스화면”에 별도 게시합니다.</p>
                                    <p>제5조 (“안심페이” 서비스)</p>
                                    <p>① “안심페이”는 이용자가 “가맹점”에서 재화나 용역을 구매, 결제시 사용하고자 하는 신용카드 정보(신용카드번호, 유효기간, 카드비밀번호 등)를 회사가 지정하는 인증수단을 사용하여 결제 인증 후, 구매대금을 간편하게 결제할 수 있는 서비스를 내용으로 합니다.</p>
                                    <p>② 이용자가 “가맹점” 중 하나에서 회사와 “안심페이” 이용계약이 성립된 경우, 이용자는 다른 가맹점에서 별도의 이용계약의 신청, 신용카드 회사나 금융기관으로부터 등록절차 없이 “안심페이”를 이용할 수 있습니다.</p>
                                    <p>③“안심페이”의 자세한 이용절차 및 거래내역 확인 서비스 등의 구체적 내용은 “서비스화면”에 별도 게시합니다.</p>
                                    <p>④ 회사는 컴퓨터 등 정보통신설비의 보수점검, 교체 및 고장, 통신두절 또는 운영상 상당한 이유가 있는 경우 “안심페이”의 제공을 일시적으로 중단하고, 이용자에게 통지합니다.</p>
                                    <p>④ 회사는 필요한 경우 이용자에 사전 고지하고 “안심페이”를 통해 제공하는 서비스의 일부 또는 전부를 삭제, 추가하거나 변경할 수 있습니다.</p>
                                    <p>제3장 기타</p>
                                    <p>제6조 (회사의 의무)</p>
                                    <p>① 회사는 본 약관에서 정한 바에 따라 “안심페이”를 계속적, 안정적으로 제공하도록 노력하여야 합니다.</p>
                                    <p>② 회사는 관련 법령에서 정한 바에 따라 이용자의 개인정보를 관리하여야 하며, 이에 대한 세부적인 내용은 회사의 “개인정보보호방침’에 정한 바에 의합니다.</p>
                                    <p>제7조 (이용자의 의무)</p>
                                    <p>① 이용자는 “안심페이 서비스” 이용 시 다음 각호 중 1에 해당하는 행위를 해서는 안 됩니다.</p>
